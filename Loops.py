# Loops in python : For and while loops.

example = ['green', 'blue', 'white', 'red']

print("Using while loop : ")
n = 0
while n < 4:
    print(example[n])
    n += 1

print("Using for loop : ")
for i in example:
    print(i)

# Looping with range values.
print("Using for loop with range : ")
for i in range(10, 15):
    print(i)

for i in range(5):
    print(i)
